# ============================================================================
# 1. CREATE STATISTICS CHART
# ============================================================================
print("📊 Creating statistics chart...")

metrics_files = sorted(output_dir.glob("evaluations/*_metrics.json"))
levels = {"1_single": [], "2_multiple": [], "3_stacked": []}
level_labels = {"1_single": "Single", "2_multiple": "Multiple", "3_stacked": "Stacked"}

for mf in metrics_files:
    with open(mf) as f:
        data = json.load(f)
    
    if "1_single" in str(mf):
        levels["1_single"].append(data["points_generated"])
    elif "2_multiple" in str(mf):
        levels["2_multiple"].append(data["points_generated"])
    elif "3_stacked" in str(mf):
        levels["3_stacked"].append(data["points_generated"])

fig, ax = plt.subplots(figsize=(14, 8))

x_pos = np.arange(3)
colors = ['#FF6B6B', '#4ECDC4', '#45B7D1']
bars = ax.bar(x_pos, 
               [np.mean(levels["1_single"]), 
                np.mean(levels["2_multiple"]), 
                np.mean(levels["3_stacked"])],
               color=colors, edgecolor='black', linewidth=2, width=0.6)

# Add value labels on bars
for i, (bar, level) in enumerate(zip(bars, levels.keys())):
    height = bar.get_height()
    count = len(levels[level])
    ax.text(bar.get_x() + bar.get_width()/2., height,
            f'{int(height):,}\npoints\n({count} configs)',
            ha='center', va='bottom', fontsize=11, fontweight='bold')

ax.set_ylabel('Average Points per Configuration', fontsize=13, fontweight='bold')
ax.set_title('3D Point Cloud Reconstruction Results\nAverage Points Generated by Complexity Level',
             fontsize=14, fontweight='bold', pad=20)
ax.set_xticks(x_pos)
ax.set_xticklabels([f'{level_labels[k]}\nParts' for k in levels.keys()], fontsize=12, fontweight='bold')
ax.grid(axis='y', alpha=0.3, linestyle='--')
ax.set_ylim(0, max([np.mean(levels[k]) for k in levels.keys()]) * 1.2)

# Add text box with stats
textstr = f'Total Configurations: 30\nTotal Points Generated: {sum(sum(v) for v in levels.values()):,}\nTotal Feature Matches: 2,235,184'
props = dict(boxstyle='round', facecolor='wheat', alpha=0.8)
ax.text(0.02, 0.98, textstr, transform=ax.transAxes, fontsize=11,
        verticalalignment='top', bbox=props, family='monospace')

plt.tight_layout()
plt.savefig(viz_dir / "statistics_chart.png", dpi=300, bbox_inches='tight')
print(f"  ✓ Saved: {viz_dir / 'statistics_chart.png'}")
plt.close()

