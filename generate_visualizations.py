# 4. CREATE POINT COUNT DISTRIBUTION CHART
# ============================================================================
print("📈 Creating point count distribution...")

all_points = []
for mf in metrics_files:
    with open(mf) as f:
        data = json.load(f)
        all_points.append(data["points_generated"])

fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 6))

# Histogram
ax1.hist(all_points, bins=15, color='#4ECDC4', edgecolor='black', linewidth=1.5, alpha=0.7)
ax1.axvline(np.mean(all_points), color='red', linestyle='--', linewidth=2, label=f'Mean: {np.mean(all_points):,.0f}')
ax1.axvline(np.median(all_points), color='green', linestyle='--', linewidth=2, label=f'Median: {np.median(all_points):,.0f}')
ax1.set_xlabel('Points per Configuration', fontsize=12, fontweight='bold')
ax1.set_ylabel('Frequency', fontsize=12, fontweight='bold')
ax1.set_title('Distribution of Generated Points', fontsize=13, fontweight='bold')
ax1.legend(fontsize=11)
ax1.grid(alpha=0.3)

# Box plot
level_1 = [all_points[i] for i in range(30) if i % 3 == 0]
level_2 = [all_points[i] for i in range(30) if i % 3 == 1]
level_3 = [all_points[i] for i in range(30) if i % 3 == 2]

bp = ax2.boxplot([level_1, level_2, level_3], labels=['Single', 'Multiple', 'Stacked'],
                   patch_artist=True, widths=0.6)

for patch, color in zip(bp['boxes'], ['#FF6B6B', '#4ECDC4', '#45B7D1']):
    patch.set_facecolor(color)
    patch.set_alpha(0.7)

ax2.set_ylabel('Points per Configuration', fontsize=12, fontweight='bold')
ax2.set_title('Point Distribution by Complexity Level', fontsize=13, fontweight='bold')
ax2.grid(alpha=0.3, axis='y')

